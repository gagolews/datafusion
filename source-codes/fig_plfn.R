library("FuzzyNumbers")
pdf('figures/Example_knot3.pdf', width=6, height=4)
par(mar=c(4,4,1,1))
a = c(0.3, 0.5, 0.7)
s = c(1, 1.5, 2, 2.4, 2.5, 4, 5, 5.5, 6.5, 7)
A = PiecewiseLinearFuzzyNumber(knot.alpha=a, knot.left=s[1:5], knot.right=s[6:10])
plot(A, las=1, xlim=c(0,8))
abline(h=a, lty=2, col="gray")
abline(v=s, lty=2, col="gray")
points(s[1:5], c(0,a,1))
points(s[6:10], rev(c(0,a,1)))
dev.off()
